package com.bko.domain;

public class PatchMember {
	private String patchId;
	private String patchMember;
	private String memberType;
	private String typAct;
	public String getPatchId() {
		return patchId;
	}
	public void setPatchId(String patchId) {
		this.patchId = patchId;
	}
	public String getPatchMember() {
		return patchMember;
	}
	public void setPatchMember(String patchMember) {
		this.patchMember = patchMember;
	}
	public String getMemberType() {
		return memberType;
	}
	public void setMemberType(String memberType) {
		this.memberType = memberType;
	}
	public String getTypAct() {
		return typAct;
	}
	public void setTypAct(String typAct) {
		this.typAct = typAct;
	}

}
